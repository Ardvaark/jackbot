package net.ardvaark.jackbot;

public class JackBotConfigurationException extends JackBotException
{
    private static final long serialVersionUID = 1L;

    public JackBotConfigurationException()
    {
        super();
    }

    public JackBotConfigurationException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public JackBotConfigurationException(String message)
    {
        super(message);
    }

    public JackBotConfigurationException(Throwable cause)
    {
        super(cause);
    }
}
